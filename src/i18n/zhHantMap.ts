/**
 * Convert Simplified Chinese text to Traditional Chinese.
 * Preserves LaTeX math ($...$), HTML tags, and non-Chinese characters.
 */
export function toTraditional(text: string): string {
  let result = '';
  let inMath = false;

  for (let i = 0; i < text.length; i++) {
    const ch = text[i];

    // Toggle math mode on $ (skip LaTeX content)
    if (ch === '$') {
      inMath = !inMath;
      result += ch;
      continue;
    }

    // Inside math mode or non-Chinese: pass through
    if (inMath) {
      result += ch;
      continue;
    }

    // Convert if mapping exists
    result += S2T[ch] || ch;
  }

  return result;
}
